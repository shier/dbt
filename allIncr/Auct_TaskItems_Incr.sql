With hashData as (
		Select
			*,HASHBYTES('SHA2_256', concat(Cast([TaskItemID] as varchar), Cast([TaskItemTypeID] as varchar), Cast([TaskID] as varchar), Cast([Created] as varchar), Cast([CompletedByUserID] as varchar), Cast([Completed] as varchar), [Message], Cast([CreatedByUserID] as varchar), Cast([AssignedByUserID] as varchar), [NavigateURL])) as dbt_scd_id,
			CURRENT_TIMESTAMP as dbt_updated_at, 
			CURRENT_TIMESTAMP as dbt_valid_from, 
			NULL as dbt_valid_to
		From stg.[Auct_TaskItems_InterView]
	)
Select * From hashData
{% if is_incremental() %}
	where not exists 
	(
		select dbt_scd_id 
		from {{ this }} compareData
		where hashData.dbt_scd_id=compareData.dbt_scd_id
	)
{% endif %}
