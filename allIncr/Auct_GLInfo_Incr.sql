With hashData as (
		Select
			*,HASHBYTES('SHA2_256', concat(Cast([GLInfoID] as varchar), Cast([AccountID] as varchar), [Name], Cast([Task_ItemType] as varchar), Cast([Created] as varchar), Cast([GlCategoryID] as varchar), Cast([FeeTypeID] as varchar), Cast([Dept] as varchar), [Tasks], [Category], [Deptxx])) as dbt_scd_id,
			CURRENT_TIMESTAMP as dbt_updated_at, 
			CURRENT_TIMESTAMP as dbt_valid_from, 
			NULL as dbt_valid_to
		From stg.[Auct_GLInfo_InterView]
	)
Select * From hashData
{% if is_incremental() %}
	where not exists 
	(
		select dbt_scd_id 
		from {{ this }} compareData
		where hashData.dbt_scd_id=compareData.dbt_scd_id
	)
{% endif %}
