{{ config(materialized='incremental',schema='stg',unique_key='dbt_scd_id')}}
With hashData as (
		Select
			*,HASHBYTES('SHA2_256', concat(Cast([PropertyTransactionID] as varchar), Cast([PropertyID] as varchar), Cast([PropertyStatusID] as varchar), [RecipientName], Cast([TransactionTime] as varchar), Cast([UserID] as varchar), Cast([PropertyTypeKey] as varchar), [Description], [PropertySpace], [ChangedFields], Cast([ConsignmentID] as varchar))) as dbt_scd_id,
			CURRENT_TIMESTAMP as dbt_updated_at, 
			CURRENT_TIMESTAMP as dbt_valid_from, 
			NULL as dbt_valid_to
		From stg.[Auct_PropertyTransaction_InterView]
	)
Select * From hashData
{% if is_incremental() %}
	where not exists 
	(
		select dbt_scd_id 
		from {{ this }} compareData
		where hashData.dbt_scd_id=compareData.dbt_scd_id
	)
{% endif %}
