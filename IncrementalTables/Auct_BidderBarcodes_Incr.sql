With hashData as (
		Select
			*, HASHBYTES('SHA2_256', concat(Cast([TicketID] as varchar), [TransactionID], [EventName], [SectionName], [Seatrow], [Seatnum], [Barcode], Cast([Length] as varchar), Cast([BidderID] as varchar), Cast([AuctionID] as varchar))) as dbt_scd_id,
			CURRENT_TIMESTAMP as dbt_valid_at,
			CURRENT_TIMESTAMP as dbt_valid_from, 
			NULL as dbt_valid_to
		From stg.[Auct_BidderBarcodes_InterView]
	)
Select * From hashData
{% if is_incremental() %}
	where not exists 
	(
		select dbt_scd_id 
		from {{ this }} compareData
		where hashData.dbt_scd_id=compareData.dbt_scd_id
	)
{% endif %}
