With hashData as (
		Select
			*, HASHBYTES('SHA2_256', concat(Cast([OAuthAuthorizationID] as varchar), [ProvIDErName], [ApiURL], [ApiVersionSuffixURL], [ClientID], [ClientSecret], [RedirectURI], [OAuthaccessToken], [OAuthRefreshToken], Cast([OAuthTokenExpirationMinutes] as varchar), [TokenType])) as dbt_scd_id,
			CURRENT_TIMESTAMP as dbt_valid_at,
			CURRENT_TIMESTAMP as dbt_valid_from, 
			NULL as dbt_valid_to
		From stg.[Auct_OAuthAuthorization_InterView]
	)
Select * From hashData
{% if is_incremental() %}
	where not exists 
	(
		select dbt_scd_id 
		from {{ this }} compareData
		where hashData.dbt_scd_id=compareData.dbt_scd_id
	)
{% endif %}
