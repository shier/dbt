With hashData as (
		Select
			*, HASHBYTES('SHA2_256', concat(Cast([OutgoingRequestQueueID] as varchar), [URL], [HttpMethod], [HttpHeaders], [RequestPayload], [ResponsePayload], Cast([ResponsehttpStatusCode] as varchar), [Status], Cast([StatusDate] as varchar), [InvoiceID], [DocumentID])) as dbt_scd_id,
			CURRENT_TIMESTAMP as dbt_valid_at,
			CURRENT_TIMESTAMP as dbt_valid_from, 
			NULL as dbt_valid_to
		From stg.[Auct_OutgoingRequestQueue_InterView]
	)
Select * From hashData
{% if is_incremental() %}
	where not exists 
	(
		select dbt_scd_id 
		from {{ this }} compareData
		where hashData.dbt_scd_id=compareData.dbt_scd_id
	)
{% endif %}
