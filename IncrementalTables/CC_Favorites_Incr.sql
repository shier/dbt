With hashData as (
		Select
			*, HASHBYTES('SHA2_256', concat(Cast([IsActive] as varchar), Cast([CreateDateUTC] as varchar), Cast([ModifyDateUTC] as varchar), Cast([FavoritePK] as varchar), Cast([ListingFK] as varchar), Cast([UserFK] as varchar), [DataTokenID])) as dbt_scd_id,
			CURRENT_TIMESTAMP as dbt_valid_at,
			CURRENT_TIMESTAMP as dbt_valid_from, 
			NULL as dbt_valid_to
		From stg.[CC_Favorites_InterView]
	)
Select * From hashData
{% if is_incremental() %}
	where not exists 
	(
		select dbt_scd_id 
		from {{ this }} compareData
		where hashData.dbt_scd_id=compareData.dbt_scd_id
	)
{% endif %}
