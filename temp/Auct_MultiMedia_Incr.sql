With hashData as (
		Select
			*, HASHBYTES('SHA2_256', concat(Cast([MultiMediaID] as varchar), Cast([ContentID] as varchar), Cast([MultiMediaTypeID] as varchar), [FileName], Cast([DateCreated] as varchar), Cast([Active] as varchar), Cast([MultiMediaCategoryID] as varchar), [Name], Cast([PathID] as varchar), [SubPath])) as dbt_scd_id,
			CURRENT_TIMESTAMP as dbt_valid_at,
			CURRENT_TIMESTAMP as dbt_valid_from, 
			NULL as dbt_valid_to
		From stg.[Auct_MultiMedia_InterView]
	)
Select * From hashData
{% if is_incremental() %}
	where not exists 
	(
		select dbt_scd_id 
		from {{ this }} compareData
		where hashData.dbt_scd_id=compareData.dbt_scd_id
	)
{% endif %}
