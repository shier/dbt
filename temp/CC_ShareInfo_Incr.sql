{{ config(materialized='incremental',schema='stg',unique_key='dbt_scd_id')}}
With hashData as (
		Select
			*, HASHBYTES('SHA2_256', concat([Browser], [Appused], [UserAgent], [UserEmail], [IpAddress], Cast([Latitude] as varchar), Cast([Longitude] as varchar), Cast([CreateDateUTC] as varchar), Cast([ShareInfoID] as varchar), Cast([UserID] as varchar), Cast([ListingFK] as varchar))) as dbt_scd_id,
			CURRENT_TIMESTAMP as dbt_valid_at,
			CURRENT_TIMESTAMP as dbt_valid_from, 
			NULL as dbt_valid_to
		From stg.[CC_ShareInfo_InterView]
	)
Select * From hashData
{% if is_incremental() %}
	where not exists 
	(
		select dbt_scd_id 
		from {{ this }} compareData
		where hashData.dbt_scd_id=compareData.dbt_scd_id
	)
{% endif %}
